package vo;

public class Basket {
	private int basketNo;
	private String customerId;
	private String productName;
	private String categoryName;
	private int productNumber;
	private int price;
	private int quantity;
	private String createDate;
	private String updateDate;
	
	@Override
	public String toString() {
		return "Basket [basketNo=" + basketNo + ", customerId=" + customerId + ", productName=" + productName
				+ ", categoryName=" + categoryName + ", productNumber=" + productNumber + ", price=" + price
				+ ", quantity=" + quantity + ", createDate=" + createDate + ", updateDate=" + updateDate + "]";
	}

	public int getBasketNo() {
		return basketNo;
	}

	public void setBasketNo(int basketNo) {
		this.basketNo = basketNo;
	}

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public int getProductNumber() {
		return productNumber;
	}

	public void setProductNumber(int productNumber) {
		this.productNumber = productNumber;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
}